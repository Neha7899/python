"""for loop to print the elements in a list"""

fruits=["apple","banana","cherry","dragonfruit"]
for fruit in fruits:
    print(fruit)


"""length of a list and index of a list"""

fruits=["apple","banana","cherry","dragonfruit"]
for idx in range(0,len(fruits)):
    print(len(fruits))
    print(idx,fruits[idx])





